class DRSTrainer(AdaTrainer):
    def __init__(self, **kwargs):
        super().__init__(**kwargs)

    def compute_loss(self, model, inputs, return_outputs=False):
        """
        How the loss is computed by Trainer. By default, all models return the loss in the first element.

        Subclass and override for custom behavior.
        """
        if self.label_smoother is not None and "labels" in inputs:
            labels = inputs.pop("labels")
        else:
            labels = None
        outputs = model(**inputs)
        # Save past state if it exists
        if self.args.past_index >= 0:
            self._past = outputs[self.args.past_index]

        if labels is not None:
            if unwrap_model(model)._get_name() in MODEL_FOR_CAUSAL_LM_MAPPING_NAMES.values():
                loss = self.label_smoother(outputs, labels, shift_labels=True)
            else:
                loss = self.label_smoother(outputs, labels)
        else:
            if isinstance(outputs, dict) and "loss" not in outputs:
                raise ValueError(
                    "The model did not return a loss from the inputs, only the following keys: "
                    f"{','.join(outputs.keys())}. For reference, the inputs it received are {','.join(inputs.keys())}."
                )
            # We don't use .loss here since the model may return tuples instead of ModelOutput.
            loss = outputs["loss"] if isinstance(outputs, dict) else outputs[0]

        # 安全计算正则项
        regu_loss = self.compute_svd_orth_regu(model)

        # 加权求和（确保类型和设备一致）
        loss = loss + self.model.peft_config['default'].orth_reg_weight * regu_loss.to(loss.device)


        # mrpc 3450 600 1800 => [600, 1650]
        # stsb 4500 800 2000 => [800, 2500] now
        # rte 3900 600 1800 => [600, 2100]
        # cola 6700 800 3500 => [800, 3200]
        # sst2 50520 6000 22000 => [6000, 28520]

        #TODO: hard code, need add param or mul a rate
        if self.state.global_step >= 6000 and self.state.global_step < 28520:
            #middle add polar_loss
            # print("!!!!!!!!!!!")
            RS_loss = self.compute_polar_RS(model, 0)
            Lnorm_loss = self.compute_polar_Lnorm(model, 2) # L2
            loss = loss + Lnorm_loss + 0.0001*RS_loss

        return (loss, outputs) if return_outputs else loss

    def compute_polar_RS(self, model, t):
        num_param = 0
        p_sum = 0
        avg = 0
        RS = 0
        # calculate avg
        for n,p in model.named_parameters():
            if "lora_E" in n:
                # calculate p_sum
                p_sum += torch.sum(p)
                num_param += p.numel()
        avg = p_sum / num_param
        # calculate RS and L2
        for n,p in model.named_parameters():
            if "lora_E" in n:
                RS_item = t * torch.sum(torch.abs(p)) - torch.sum(torch.abs(p-avg))
                RS += RS_item
        return RS

    def compute_polar_Lnorm(self, model, Lnum):
        all_lora_E = []
        num_param = 0
        for n,p in model.named_parameters():
            if "lora_E" in n:
                all_lora_E.append(p.flatten())
                num_param += p.numel()
        L2_sum = torch.norm(torch.cat(all_lora_E), p=Lnum)
        norm_L2_sum = L2_sum / num_param
        return norm_L2_sum

class LoRAFreezeCallback(TrainerCallback):
    def __init__(self, model, adapter_name, num_epoches, l_num, scale=None):
        self.model = model
        self.schedule = []
        self.current_adapter = None
        self.adapter_name = adapter_name
        if scale is not None:
            self.scale = scale
        else:
            self.scale = [1.0 / l_num for i in range(0, l_num)]
        for i in range(0, l_num):
            if not i:
                start = 0
            end = start + self.scale[i] * num_epoches
            self.schedule.append((start, end, adapter_name + f"_{i}"))
            start = end
        print("------------Train Schedule Init Successfully!!!------------")
        for msp_start, msp_end, msp_adapter_name in self.schedule:
            print(f"------------Start:{msp_start}! End:{msp_end}! Adaptername:{msp_adapter_name}!------------")

    def on_epoch_begin(self, args, state, control, **kwargs):
        epoch = state.epoch if state.epoch is not None else 0
        adapter_to_unfreeze = None
        for start, end, adapter_name in self.schedule:
            if start <= epoch < end:
                adapter_to_unfreeze = adapter_name
                break

        if adapter_to_unfreeze is None:
            return

        if adapter_to_unfreeze == self.current_adapter:
            return

        self.current_adapter = adapter_to_unfreeze
        print(f"Epoch {epoch}: Unfreezing LoRA parameters with adapter '{adapter_to_unfreeze}' and freezing others.")

        for name, param in self.model.named_parameters():
             if "lora_" in name:
                if self.current_adapter not in name:
                    param.requires_grad = False
                else:
                    param.requires_grad = True
        for name, param in self.model.named_parameters():
            if param.requires_grad:
                # print(f"Parameter {name} is not frozen.")
                pass

